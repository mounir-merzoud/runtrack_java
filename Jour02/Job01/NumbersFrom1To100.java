package Job01; // Définition du package Job01

public class NumbersFrom1To100 {
    public static void main(String[] args) {
        // Utilisation d'une boucle for pour afficher les chiffres de 1 à 100
        for (int i = 0; i <= 100; i++) {
            System.out.println(i); // Affiche chaque chiffre sur une nouvelle ligne
        }
    }
}


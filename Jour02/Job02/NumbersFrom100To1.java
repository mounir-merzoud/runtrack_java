package Job02; // Définition du package Job02

public class NumbersFrom100To1 {
    public static void main(String[] args) {
        // Utilisation d'une boucle for pour afficher les chiffres de 100 à 1
        for (int i = 101; i >= 1; i--) {
            System.out.println(i); // Affiche chaque chiffre sur une nouvelle ligne
        }
    }
}
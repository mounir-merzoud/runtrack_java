package Job07;

public class Addition {
    public static void main(String[] args) {
        // Déclare deux variables entières avec les valeurs 3 et 73
        int valeur1 = 3;
        int valeur2 = 73;

        // Effectue la division de valeur2 par valeur1
        double resultat = (double) valeur2 / valeur1; // Convertit en double pour une division précise

        // Affiche le résultat de la division
        System.out.println("Résultat de la division de " + valeur2 + " par " + valeur1 + " : " + resultat);
    }
}

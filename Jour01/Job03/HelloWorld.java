// Ce fichier contient un programme Java simple pour afficher "Hello, world" à l'écran.

public class HelloWorld {
    public static void main(String[] args) {
        // Cette ligne de code affiche "Hello, world" dans la console.
        System.out.println("Hello, world");

        /*
         * Le code ci-dessus utilise la méthode System.out.println
         * pour imprimer du texte dans la console.
         * La méthode 'println' ajoute également une nouvelle ligne après le texte.
         */
    }
}

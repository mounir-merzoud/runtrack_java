
public class DeclarationVariable {

    public static void main(String[] args) {
        // Déclarer trois variables de type int sur une seule ligne
        int a, b, c;

        // Assigner la valeur 0 à chaque variable sur une autre ligne
        a = 0;
        b = 0;
        c = 0;

        // Afficher les valeurs pour vérifier
        System.out.println("Valeurs des variables :");
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}

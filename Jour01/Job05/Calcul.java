package Job05;

public class Calcul {

    

    
    public static void main(String[] args) {
        
        int nombre = 7;

        
        System.out.println("Valeur initiale de nombre: " + nombre);

        
        nombre += 3;
        
        System.out.println("Après avoir ajouté 3: " + nombre);

        
        nombre *= 5;
        
        System.out.println("Après avoir multiplié par 5: " + nombre);
    }
}
